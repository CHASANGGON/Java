package cond;

public class cond1 {
    public static void main(String[] args) {
        int a = 10;
        if (a > 10) {
            System.out.println("a는 10보다 크다");
        } else if (a == 10) {
            System.out.println("a는 10과 같다");
        } else {
            System.out.println("a는 10보다 작다");
        }
    }
}
